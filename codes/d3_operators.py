# arithmetic operators
number_1 = 100
number_2 = 200

result_value=number_1+number_2 # addition
print(result_value)
result_value=number_1-number_2 # minus
print(result_value)
result_value=number_1*number_2 # multiplication
print(result_value)
result_value=number_1/number_2 # decimal division
print(result_value)
result_value=number_1//number_2 # Floor Division
print(result_value)
result_value=number_1**number_2 # Exponent
print(result_value)
result_value=number_1%number_2 # Remainder, modulus
print(result_value)
